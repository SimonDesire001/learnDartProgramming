void main(){

  // switch statement
  int age = 18;
  switch(age){
    case 18:
      print("You are 18");
      break;
    case 19:
      print("You are 19");
      break;
    case 20:
      print("You are 20");
      break;
    default:
      print("You are not 18, 19 or 20");
  }
}